public class ProvinceEntity {

	/**
	 * 省份ID
	 */
	private String id;

	/**
	 * 省份名称
	 */
	private String province;

	/**
	 * 显示排序号
	 */
	private int indexId;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the province
	 */
	public String getProvince() {
		return province;
	}

	/**
	 * @param province
	 *            the province to set
	 */
	public void setProvince(String province) {
		this.province = province;
	}

	/**
	 * @return the indexId
	 */
	public int getIndexId() {
		return indexId;
	}

	/**
	 * @param indexId
	 *            the indexId to set
	 */
	public void setIndexId(int indexId) {
		this.indexId = indexId;
	}

}