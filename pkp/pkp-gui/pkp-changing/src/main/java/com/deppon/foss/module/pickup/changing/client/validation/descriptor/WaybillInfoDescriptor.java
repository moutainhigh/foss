	/**
	 * 
	 * <p>
	 * (货物品名校验-违禁品)
	 * </p>
	 * 
	 * @author 025000-FOSS-helong
	 * @date 2012-10-20 上午09:42:03
	 * @param goodsName
	 * @return
	 * @see
	 */
	public String validateGoodsName(String goodsName, WaybillPanelVo bean) {
		if(!ui.getWaybillInfoPanel().getGoodsPanel().getTxtName().isEnabled()){
			return WaybillConstants.SUCCESS;
		}
		if(goodsName!=null&&!"".equals(goodsName)){
			boolean bool = false;    		
			List<ProhibitedArticlesEntity> list = new ArrayList<ProhibitedArticlesEntity>();
    		ProductEntityVo productVo = bean.getProductCode();
    		/**
			 * 判断运输性质是否为空
			 */
    		if (null != productVo) {
    			/**
    			 * 判断运输性质是否为空运
    			 */
    			if (ProductEntityConstants.PRICING_PRODUCT_AIR_FREIGHT.equals(productVo.getCode())) {
    				//获取空运违禁品
    				list = waybillRfcService.queryProhibitGoodsByType(DictionaryValueConstants.AIR_CONTRABAND);
        		}else{
        			//获取汽运违禁品
        			list = waybillRfcService.queryProhibitGoodsForAutomobile();
        		}
    		}else{
    			//获取所有违禁品
    			list = waybillRfcService.queryAllProhibitGoods();
    		}
    		if(list!=null&&list.size()>0){
    			for(int i=0;i<list.size();i++){
    				ProhibitedArticlesEntity entity = list.get(i);
    				if(!StringUtil.isEmpty(entity.getKeyWords())){
    					if(goodsName.contains(entity.getKeyWords())){
    						bool = true;
    						break;
    					}
    				}
    			}
    		}
    		if (bool) {
    			return i18n.get("foss.gui.changing.waybillDescriptor.embargo.name");
    		}
		}else
		{
			return i18n.get("foss.gui.changing.descriptor.Waybill.goodsName.null");
		}
		return WaybillConstants.SUCCESS;
	}
	
	/**
	 * 
	 * <p>
	 * (发货客户手机号码输入值校验)
	 * </p>
	 * 
	 * @author 025000-FOSS-helong
	 * @date 2012-10-20 上午09:43:42
	 * @param goodsName
	 * @return
	 * @see
	 */
	public String validateDeliveryCustomerMobilephone(String deliveryCustomerMobilephone,WaybillInfoVo waybillInfoVo) {
		if (StringUtil.isNotEmpty(deliveryCustomerMobilephone)) {
			int phoneLength = deliveryCustomerMobilephone.trim().length();
			
			int lengthLimitEight = EIGHT;
			int lengthLimitEleven = ElEVEN;
			
			if (phoneLength != lengthLimitEight && phoneLength != lengthLimitEleven) {
				return i18n.get("foss.gui.creating.waybillDescriptor.deliver.mobile");
			}
			
			if (phoneLength == lengthLimitEleven) {
				if (!"1".equals(deliveryCustomerMobilephone.substring(0, 1))) {
					return i18n.get("foss.gui.creating.waybillDescriptor.deliver.mobile.one");
				}
			}
			//判定手机号码与电话号码是否存在一致的情况：存在电话号码组成：0+手机号码这种情况，需要过滤掉
			if(StringUtils.isNotEmpty(waybillInfoVo.getDeliveryCustomerPhone())){
				if(deliveryCustomerMobilephone.equals(waybillInfoVo.getDeliveryCustomerPhone())){
					return i18n.get("foss.gui.creating.listener.Waybill.telephoneInMobilePhoneisExist");
				}
				try {
					String deliveryCustomerPhone = waybillInfoVo.getDeliveryCustomerPhone().replaceFirst("^0*", ""); 
					if (StringUtils.isNotEmpty(deliveryCustomerPhone)//不为空
							&& (deliveryCustomerPhone.length() == NumberConstants.NUMBER_8//并且满足手机号码的校验条件
							|| (deliveryCustomerPhone.length() == NumberConstants.NUMBER_11 && deliveryCustomerPhone.startsWith(String.valueOf(1))))) {
						if(StringUtils.isNotEmpty(waybillInfoVo.getDeliveryCustomerMobilephone())
								&& deliveryCustomerMobilephone.equals(deliveryCustomerPhone)){
							return i18n.get("foss.gui.creating.listener.Waybill.telephoneInMobilePhoneisExist");
						}
					}
				} catch (Exception e) {
					LOG.info("数据转化异常，不需要进行数据的校验");
				}
			}
		}
		return WaybillConstants.SUCCESS;
	}
	

