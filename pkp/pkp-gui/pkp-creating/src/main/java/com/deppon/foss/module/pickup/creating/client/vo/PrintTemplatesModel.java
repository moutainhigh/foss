package com.deppon.foss.module.pickup.creating.client.vo;


/**
 * 打印模版数据模型
 * 
 * @author 097972-foss-dengtingting
 * @date 2013-1-26 下午5:11:24
 */
public class PrintTemplatesModel{
	
	//打印模版key
	private String key;
	
	//打印模版value
	private String value;
	
	public PrintTemplatesModel(String key,String value){
		this.key = key;
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String toString()
    {
        return value;
    }
}