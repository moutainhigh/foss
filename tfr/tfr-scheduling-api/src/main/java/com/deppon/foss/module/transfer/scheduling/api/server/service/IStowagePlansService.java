	/** 
	 * 查询 配载方案配置 count 
	 * @author 134019-foss-yuyongxiang
	 * @date 2013年7月31日 16:09:13
	 * @param stowagePlansDto
	 * @return List
	 */
	Long selectStowagePlansListCount(StowagePlansDto stowagePlansDt);
	/**
	 * 查询 配载方案配置 明细
	 * @author 134019-foss-yuyongxiang
	 * @date 2013-7-30 下午2:39:29
	 * @param stowagePlansDto
	 * @return List
	 */
