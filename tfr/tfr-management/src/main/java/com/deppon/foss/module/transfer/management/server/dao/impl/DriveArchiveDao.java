package com.deppon.foss.module.transfer.management.server.dao.impl;

import java.util.List;

import org.apache.ibatis.session.RowBounds;

import com.deppon.foss.framework.server.components.dataaccess.ibatis.iBatis3DaoImpl;
import com.deppon.foss.module.transfer.management.api.server.dao.IDriveArchiveDao;
import com.deppon.foss.module.transfer.management.api.shared.domain.DriveArchiveEntity;
import com.deppon.foss.module.transfer.management.api.shared.dto.DriveArchiveDto;
import com.deppon.foss.module.transfer.management.api.shared.dto.DriveArchiveVehicleInfoDto;

@SuppressWarnings("unchecked")
public class DriveArchiveDao extends iBatis3DaoImpl implements IDriveArchiveDao {

	private static final String nameSpace = "foss.management.driverarchive.";
	
	/**
	 * 根据ID删除行驶档案信息
	 * @author foss-liuxue(for IBM)
	 * @date 2012-11-8 上午10:44:41
	 */
	@Override
	public int deleteByPrimaryKey(String id) {
		//根据ID删除行驶档案信息
		return getSqlSession().delete(nameSpace+"deleteByPrimaryKey",id);
	}

	/**
     * 根据ID查询详细信息
     * @author foss-liuxue(for IBM)
     * @date 2012-11-8 上午10:45:06
     */
	@Override
	public List<DriveArchiveEntity> displayDriveArchiveDetail(String id) {
		//根据ID查询详细信息
		return getSqlSession().selectList(nameSpace+"selectByPrimaryKey",id);
	}

	/**
     * 查询所有行驶档案信息
     * @author foss-liuxue(for IBM)
     * @date 2012-11-8 上午10:45:19
     */
	@Override
	public List<DriveArchiveEntity> queryDriveArchive(DriveArchiveDto driveArchiveDto,int start,int limit) {
		RowBounds rowBounds = new RowBounds(start,limit);
		//查询所有行驶档案信息
		return getSqlSession().selectList(nameSpace+"queryAll",driveArchiveDto,rowBounds);
	}

	/**
     * 获取总记录条数
     * @author foss-liuxue(for IBM)
     * @date 2012-11-8 上午10:45:41
     */
	@Override
	public Long getCount(
			DriveArchiveDto driveArchiveDto) {
		//获取总记录条数
		return (Long)getSqlSession().selectOne(nameSpace+"getCount",driveArchiveDto);
	}
	
	/**
     * 新增行驶档案
     * @author foss-liuxue(for IBM)
     * @date 2012-11-8 上午10:45:54
     */
	@Override
	public int addDriveArchive(DriveArchiveEntity driveArchiveEntity) {
		//新增行驶档案
		return getSqlSession().insert(nameSpace+"insert",driveArchiveEntity);
	}

	/**
     * 修改行驶档案
     * @author foss-liuxue(for IBM)
     * @date 2012-11-8 上午10:47:08
     */
	@Override
	public int updateDriveArchive(DriveArchiveEntity driveArchiveEntity) {
		//修改行驶档案
		return getSqlSession().update(nameSpace+"updateByPrimaryKey",driveArchiveEntity);
	}
	
	/**
	 * 根据配载号查询车辆信息
	 * @author foss-liuxue(for IBM)
	 * @date 2012-12-29 下午4:19:49
	 */
	@Override
	public List<DriveArchiveVehicleInfoDto> queryVehicleNoByVehicleAssembleNo(DriveArchiveDto driveArchiveDto){
		//根据配载号查询车辆信息
		return this.getSqlSession().selectList(nameSpace+"queryVehicleNoByVehicleAssembleNo", driveArchiveDto);
	}

}