    /** 
     * <p>添加线路</p> 
     * @author foss-zhujunyong
     * @date Oct 26, 2012 3:22:56 PM
     * @param line
     * @return 
     * @see com.deppon.foss.module.base.baseinfo.api.server.service.ILineService#addLine(com.deppon.foss.module.base.baseinfo.api.shared.domain.LineEntity)
     */
    @Override
    @Transactional
    public LineEntity addLine(LineEntity line) {
	// 检查参数
	if (line == null) {
	    return null;
	}
	// 如果是到达线路，只有汽运一种类型
	if (StringUtils.equals(DictionaryValueConstants.BSE_LINE_SORT_TARGET, line.getLineSort())) {
	    line.setTransType(DictionaryValueConstants.BSE_LINE_TRANSTYPE_QIYUN);
	}

	// 检查线路简码是否有重复
	LineEntity entity = querySimpleLineBySimpleCodeNoCache(line.getSimpleCode());
	if (entity != null) {
	    throw new LineException(LineException.SIMPLECODE_EXIST);
	}
	// 检查是否有重复属性的线路（相同出发和到达站，线路类型的默认线路是否重复等）
	validateDuplication(line, null);
	
	// 驻地营业部/派送部始发线路和到达线路只能配置给对应的驻地外场。
	validStation(line);

	// 如果是运作到运作线路的专线类型（也就是有线段的），则默认为invalid。 否则都默认为valid
	if (StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TRANSFER) 
		&& StringUtils.equals(line.getLineType(), DictionaryValueConstants.BSE_LINE_TYPE_ZHUANXIAN)) {
	    line.setValid(FossConstants.NO);
	} else {
	    line.setValid(FossConstants.YES);
	}
	//如果是到达线路
	if (StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TARGET)){
	    validateExpressOutAssemble(line.getOrginalOrganizationCode(), line.getDestinationOrganizationCode());
	}
	LineEntity lineEntity = new LineEntity();
	lineEntity = lineDao.addLine(line);
	
	 /**
     *     DMANA-2870
     *     新增始发线路、到达线路时生成默认发车标准
     *     author:187862-dujunhui
     *     date:2014-08-20 下午1:53
     * **/
	if (StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TARGET)||StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_SOURCE)){
		DepartureStandardEntity departureStandard=new DepartureStandardEntity();
		departureStandard.setOrder(1);
		departureStandard.setArriveDay(new Long(0));
		departureStandard.setLineVirtualCode(lineEntity.getVirtualCode());
		if (StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_SOURCE)){//始发线路
			departureStandard.setLeaveTime("1700");
			departureStandard.setArriveTime("1700");
		}
		if (StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TARGET)){//到达线路
			departureStandard.setLeaveTime("0800");
			departureStandard.setArriveTime("0800");
		}
		departureStandard = departureStandardService.addDefaultDepartureStandard(departureStandard);	
		//同步新增 默认发车标准 到WDGH
		syncDepartureStandardToWdgh(departureStandard,NumberConstants.ONE);
	}
	
	//如果是到达线路和始发线路
	if (StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TARGET)||StringUtils.equals(line.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_SOURCE)){
		  //同步到短途GPS
		syncLineInfoToGps(lineEntity, NumberConstants.ONE);
		//同步给wdgh 的必须是默认的  而且是汽运的
		if(StringUtils.equals(line.getTransType(), DictionaryValueConstants.BSE_LINE_TRANSTYPE_QIYUN) && StringUtils.equals(line.getIsDefault(), FossConstants.ACTIVE)){
			syncLineInfoToWdgh(lineEntity,NumberConstants.ONE);
		}
	}
	//新的WDGH同步新增线路接口，包括始发 到达 中转
	syncNewLineInfoToWdgh(lineEntity,NumberConstants.ONE);
	return lineEntity;
    }
      
