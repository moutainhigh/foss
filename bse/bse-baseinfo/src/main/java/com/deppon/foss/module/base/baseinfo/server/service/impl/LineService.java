    /** 
     * <p>作废线路</p> 
     * @author foss-zhujunyong
     * @date Oct 26, 2012 3:22:56 PM
     * @param line 只用virtualCode和modifyUser
     * @return 
     * @see com.deppon.foss.module.base.baseinfo.api.server.service.ILineService#deleteLine(com.deppon.foss.module.base.baseinfo.api.shared.domain.LineEntity)
     */
    @Override
    @Transactional
    public LineEntity deleteLine(LineEntity line) {
    LineEntity  toGpsEntity = new LineEntity();
	// 检查参数
	if (line == null) {
	    return null;
	}
	LineEntity entity = queryLineByVirtualCode(line.getVirtualCode());
	toGpsEntity = entity;
	if (entity == null){
	    return null;
	}

	// 如果是始发线路，还需要同步作废网点组
	if (StringUtils.equals(entity.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_SOURCE)) {
	  //  boolean isExist = checkSameSourceTargetLineExist(entity.getOrginalOrganizationCode(), entity.getDestinationOrganizationCode(), DictionaryValueConstants.BSE_LINE_SORT_SOURCE, line.getVirtualCode());
	  //    if (BooleanUtils.isFalse(isExist)) {
		
		//作废网点组的同时同步信息到WDGH
		netGroupService.deleteSourceNetGroup(entity.getOrginalOrganizationCode(), entity.getDestinationOrganizationCode(), line.getModifyUser(),entity.getTransType(),FossConstants.NETWORK_GROUP_LD);
		//    }
	}
	// 如果是到达线路，还需要同步作废网点组
	if (StringUtils.equals(entity.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TARGET)) {
	    boolean isExist = checkSameSourceTargetLineExist(entity.getOrginalOrganizationCode(), entity.getDestinationOrganizationCode(), DictionaryValueConstants.BSE_LINE_SORT_TARGET, line.getVirtualCode());
	    if (BooleanUtils.isFalse(isExist)) {
	    	
	    //作废网点组的同时同步信息到WDGH
		netGroupService.deleteTargetNetGroup(entity.getDestinationOrganizationCode(), entity.getOrginalOrganizationCode(), line.getModifyUser(),FossConstants.NETWORK_GROUP_LD);
	    }
	}
	
	// 如果是运作线路，专线类型，并且有效性为Y，则不能作废
	if (entity.checkValid() 
		&& StringUtils.equals(entity.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TRANSFER) 
		&& StringUtils.equals(DictionaryValueConstants.BSE_LINE_TYPE_ZHUANXIAN, entity.getLineType())) {
	    throw new LineException(LineException.VALID_LINE_CAN_NOT_BE_DELETED);
	}
	
	// 检查该线路在走货路径中是否已经引用，若引用则不让删
	checkReferenceInFreightRoute(line.getVirtualCode());
	
	//同步作废线段信息到WDGH（其中嵌套了同步线路（走货路径时效））
	lineItemService.deleteLineItemByLine(line.getVirtualCode(), line.getModifyUser());
	//根据linevirtualcode获取发车标准
	List<DepartureStandardEntity> list = departureStandardService.queryDepartureStandardListByLineVirtualCode(line.getVirtualCode());
	// 作废发车标准
	departureStandardService.deleteDepartureStandardByLine(line.getVirtualCode(), line.getModifyUser());
    Date date = new Date();
	//同步作废发车标准到WDGH
	for(DepartureStandardEntity dsEntity:list){
		
		dsEntity.setActive(FossConstants.INACTIVE);
		dsEntity.setModifyDate(date);
		dsEntity.setVersion(date.getTime());
		dsEntity.setModifyUser(line.getModifyUser());		
		syncDepartureStandardToWdgh(dsEntity,NumberConstants.THREE);
	}
	
	//根据virtualcode获取line
	LineEntity liEntity = lineDao.queryLineByVirtualCode(line.getVirtualCode());
	//作废线路信息
	entity = lineDao.deleteLine(line);

	//如果是到达线路和始发线路
	if (StringUtils.equals(toGpsEntity.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_TARGET)||StringUtils.equals(toGpsEntity.getLineSort(), DictionaryValueConstants.BSE_LINE_SORT_SOURCE)){
		//同步到短途GPS
		syncLineInfoToGps(toGpsEntity, NumberConstants.THREE);
		//同步给wdgh 的必须是默认的  而且是汽运的
		if(StringUtils.equals(toGpsEntity.getTransType(), DictionaryValueConstants.BSE_LINE_TRANSTYPE_QIYUN) && StringUtils.equals(toGpsEntity.getIsDefault(), FossConstants.ACTIVE)){
			syncLineInfoToWdgh(toGpsEntity,NumberConstants.THREE);
		}
	}
	if(null != liEntity){
		
		liEntity.setActive(entity.getActive());
		liEntity.setModifyDate(entity.getModifyDate());
		liEntity.setVersion(entity.getVersion());
		liEntity.setModifyUser(line.getModifyUser());
		//同步作废线路信息到WDGH，包括始发 到达 中转(new)
		syncNewLineInfoToWdgh(liEntity,NumberConstants.THREE);
	}
	// 清空缓存
	if (entity != null) {
	    invalidEntity(entity.getVirtualCode());
	    invalidEntity(entity.getSimpleCode());
	}
	
	return entity;
    }

